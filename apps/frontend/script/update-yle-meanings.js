async function main() {
  console.log('🚀 Bắt đầu update nghĩa tiếng Việt cho 500 từ YLE Starters...\n');

  // Lấy tất cả THEORY records của Starters
  const contents = await prisma.lessonContent.findMany({
    where: {
      contentType: 'THEORY',
      lesson: { program: { code: { contains: 'starters' } } }
    }
  });

  console.log(`📚 Tổng THEORY records: ${contents.length}\n`);

  let updated = 0, skipped = 0, notFound = 0;

  for (const c of contents) {
    try {
      const data = JSON.parse(c.content);
      const word = data.word;

      // Tìm nghĩa mới theo word (case-sensitive trước, rồi insensitive)
      const newMeaning = MEANINGS[word] || MEANINGS[word?.toLowerCase()] || MEANINGS[word?.toUpperCase()];

      if (!newMeaning) {
        notFound++;
        continue;
      }

      if (data.meaning === newMeaning) {
        skipped++;
        continue;
      }

      // Update nghĩa mới
      data.meaning = newMeaning;
      await prisma.lessonContent.update({
        where: { id: c.id },
        data: { content: JSON.stringify(data) }
      });
      updated++;
    } catch(e) {
      console.error(`❌ Lỗi record ${c.id}: ${e.message}`);
    }
  }

  console.log('========== KẾT QUẢ ==========');
  console.log(`✅ Đã update : ${updated} từ`);
  console.log(`⏭️  Bỏ qua   : ${skipped} từ (nghĩa giống nhau)`);
  console.log(`❓ Không tìm : ${notFound} từ`);
  console.log(updated > 0 ? '\n🎉 Hoàn thành update nghĩa tiếng Việt!' : '\n⚠️  Không có gì thay đổi.');
}

main().finally(() => prisma.$disconnect());
